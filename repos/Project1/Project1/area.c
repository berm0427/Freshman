//************************************************************************************************

//  원의 면적을 계산하여 출력한다.

//************************************************************************************************

#include <stdio.h>

int main()
{
	const float PI = 3.14;
	double r, area;

	r = 50.0;
	area = PI * r * r;
	printf("반지름 %8.2f인 원의 면적: %8.2f\n", r, area);

	return 0;
}