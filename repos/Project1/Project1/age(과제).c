//************************************************************************************************

//  나이의 총합과 평균을 출력하는 프로그램

//************************************************************************************************

#include <stdio.h>

int main()
{
	int age_m = 44;
	int age_f = 55;
	int age_sis = 23;
	int age_bsis = 18;
	int age_me = 21;
	int sum = age_f + age_m + age_sis + age_me + age_bsis;
	int aver = sum / 5;
	printf("제 가족의 나이의 총합은 %d이고, 평균은 %d입니다.\n", sum, aver);
}